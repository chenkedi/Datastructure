package leetcode.tree;

import java.util.List;

/**
 * 多叉树结构
 * @author chenkedi chenkedi@baidu.com
 * @date 2019/6/27
 **/
public class Node {
    public int val;
    public List<Node> children;

    public Node() {}

    public Node(int _val,List<Node> _children) {
        val = _val;
        children = _children;
    }
}
